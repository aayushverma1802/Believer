class Solution{
    public:
    /* Should return data of middle node. If linked list is empty, then  -1*/
    int traverse(Node * head){
        int count=0;
        Node *p=head;
        while(p!=NULL){
            p=p->next;
            count++;
            
        }
        return count;
    }
    int getMiddle(Node *head)
    {
        // Your code here
        int size=traverse(head);
        int s=size/2;
        int i=0;
        while(i<s){
            
            head=head->next;
            i++;
            
        }
        return head->data;
        
    }
};


